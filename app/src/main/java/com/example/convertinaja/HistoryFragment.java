/**
 * A simple {@link Fragment} subclass.
 * Use the {@link HistoryFragment#newInstance} factory method to
 * create an instance of this fragment.
 */
public class HistoryFragment extends Fragment {

    // TODO: Rename parameter arguments, choose names that match
    // the fragment initialization parameters, e.g. ARG_ITEM_NUMBER
    private static final String ARG_PARAM1 = "param1";
    private static final String ARG_PARAM2 = "param2";

    // TODO: Rename and change types of parameters
    private String mParam1;
    private String mParam2;

    public HistoryFragment() {
        // Required empty public constructor
    }

    /**
     * Use this factory method to create a new instance of
     * this fragment using the provided parameters.
     *
     * @param param1 Parameter 1.
     * @param param2 Parameter 2.
     * @return A new instance of fragment HistoryFragment.
     */
    // TODO: Rename and change types and number of parameters
    public static HistoryFragment newInstance(String param1, String param2) {
        HistoryFragment fragment = new HistoryFragment();
        Bundle args = new Bundle();
        args.putString(ARG_PARAM1, param1);
        args.putString(ARG_PARAM2, param2);
        fragment.setArguments(args);
        return fragment;
    }

    ConvertInterface convertInterface;
    RecyclerView rec;
    HistoryAdapter adapter;

    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        if (getArguments() != null) {
            mParam1 = getArguments().getString(ARG_PARAM1);
            mParam2 = getArguments().getString(ARG_PARAM2);
        }
    }

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        rec = view.findViewById(R.id.recycleViewHistory);
        rec.setLayoutManager(new LinearLayoutManager(getContext()));

        convertInterface = APIclient.getClient().create(ConvertInterface.class);

        getData();
    }

    private void getData() {

        Call<List<Convert>> getConvert = convertInterface.getConvert();
        getConvert.enqueue(new Callback<List<Convert>>() {
            @Override
            public void onResponse(Call<List<Convert>> call, Response<List<Convert>> response) {
                ArrayList<Convert> listConvert = (ArrayList<Convert>) response.body();
                Log.d("list_curhat: ", response.raw().toString());
                Log.d("list_curhat: ", listConvert.toString());

                adapter = new HistoryAdapter(listConvert);
                rec.setAdapter(adapter);
            }

            @Override
            public void onFailure(Call<List<Convert>> call, Throwable t) {
                Log.e("error_curhat: ", t.getMessage());
            }
        });
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        return inflater.inflate(R.layout.fragment_history, container, false);
    }
}